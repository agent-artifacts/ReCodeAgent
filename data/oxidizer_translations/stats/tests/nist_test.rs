fn test<T>(d: &str, r: &Result<f64, T>, a: f64, v: f64) {
    let r_val = match r {
        Ok(val) => *val,
        Err(_) => panic!("Error in {}", d),
    };

    let failure = if r_val.is_nan() || a.is_nan() {
        r_val.is_nan() != a.is_nan()
    } else if r_val.is_infinite() || a.is_infinite() {
        r_val.is_infinite() != a.is_infinite()
    } else if a != 0.0 {
        (r_val - a).abs() / a.abs() > v
    } else {
        r_val.abs() > v
    };

    if failure {
        panic!("{} => {} != {}", d, r_val, a);
    }
}
